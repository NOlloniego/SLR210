package com.example;

public class Decide {

	private int v;
	private int ballot;
	
	public Decide(int v, int ballot) {
		this.v = v;
		this.ballot = ballot;
	}

	public int getV() {
		return v;
	}

	public void setV(int v) {
		this.v = v;
	}

	public int getBallot() {
		return ballot;
	}

	public void setBallot(int ballot) {
		this.ballot = ballot;
	}
	
	
	
}
